public class Operacoes {
    public static String contador(int i, int f){
        String s = "";
        //som = VAZIO pois vai receber os valores quando chamado o método\\
        for (int c = i; c <= f; c++){
            //contador = inicio, enquanto cont <= fim contador +1\\
            s += c + " ";
            //soma recebe contador
        }
        return s;
        //retorna soma\\
        //o codigo recebe 2 números e retorna eles no tipo string\\
    }


}
